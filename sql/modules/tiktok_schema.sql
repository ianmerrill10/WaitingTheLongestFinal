-- View: Posts with calculated metrics
CREATE OR REPLACE VIEW tiktok_posts_with_metrics AS
SELECT
    tp.*,
    d.name as dog_name,
    d.breed_primary as dog_breed,
    d.urgency_level as dog_urgency,
    (tp.analytics->>'views')::bigint as view_count,
    (tp.analytics->>'likes')::bigint as like_count,
    (tp.analytics->>'shares')::bigint as share_count,
    (tp.analytics->>'comments')::bigint as comment_count,
    (tp.analytics->>'engagement_rate')::float as engagement_rate,
    CASE
        WHEN tp.scheduled_at IS NOT NULL THEN tp.scheduled_at - NOW()
        ELSE NULL
    END as time_until_scheduled
FROM tiktok_posts tp
LEFT JOIN dogs d ON tp.dog_id = d.id;

COMMENT ON VIEW tiktok_posts_with_metrics IS 'TikTok posts with extracted analytics metrics and dog info';

-- View: Scheduled posts due for posting
CREATE OR REPLACE VIEW tiktok_posts_due AS
SELECT *
FROM tiktok_posts
WHERE status = 'scheduled'
  AND scheduled_at <= NOW()
ORDER BY scheduled_at ASC;

COMMENT ON VIEW tiktok_posts_due IS 'Scheduled posts that are past their scheduled time';

-- View: Analytics summary by template type
CREATE OR REPLACE VIEW tiktok_template_performance AS
SELECT
    template_type,
    COUNT(*) as total_posts,
    COUNT(*) FILTER (WHERE status = 'published') as published_posts,
    SUM((analytics->>'views')::bigint) as total_views,
    SUM((analytics->>'likes')::bigint) as total_likes,
    SUM((analytics->>'shares')::bigint) as total_shares,
    AVG((analytics->>'engagement_rate')::float) as avg_engagement_rate
FROM tiktok_posts
WHERE status = 'published'
GROUP BY template_type
ORDER BY total_views DESC;

COMMENT ON VIEW tiktok_template_performance IS 'Performance metrics grouped by template type';

