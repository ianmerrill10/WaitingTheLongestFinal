-- Update timestamp trigger for posts
CREATE OR REPLACE FUNCTION update_tiktok_posts_timestamp()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER tiktok_posts_updated_at
    BEFORE UPDATE ON tiktok_posts
    FOR EACH ROW
    EXECUTE FUNCTION update_tiktok_posts_timestamp();

