-- ============================================================================
-- TIKTOK_POSTS TABLE
-- Purpose: Store TikTok posts with scheduling, status, and analytics
-- ============================================================================
CREATE TABLE IF NOT EXISTS tiktok_posts (
    -- Primary Key
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Relationships
    dog_id UUID NOT NULL REFERENCES dogs(id) ON DELETE CASCADE,
    shelter_id UUID REFERENCES shelters(id) ON DELETE SET NULL,

    -- Template
    template_type VARCHAR(50),              -- urgent_countdown, longest_waiting, etc.

    -- Content
    caption TEXT NOT NULL,                  -- Post caption (max 2200 chars for TikTok)
    hashtags TEXT[] DEFAULT '{}',           -- Array of hashtags
    music_id VARCHAR(100),                  -- TikTok sound ID
    music_name VARCHAR(255),                -- Sound display name

    -- Media
    content_type VARCHAR(50) DEFAULT 'video', -- video, photo_slideshow, single_photo
    video_url TEXT,                         -- Video URL for upload
    photo_urls TEXT[] DEFAULT '{}',         -- Photo URLs for slideshow
    thumbnail_url TEXT,                     -- Thumbnail image URL

    -- Overlay/Styling
    overlay_text TEXT,                      -- Text overlay content
    overlay_style VARCHAR(50),              -- Overlay style preset
    include_countdown BOOLEAN DEFAULT FALSE, -- Show urgency countdown
    include_wait_time BOOLEAN DEFAULT FALSE, -- Show wait time display

    -- Scheduling
    scheduled_at TIMESTAMP WITH TIME ZONE,  -- When to post
    posted_at TIMESTAMP WITH TIME ZONE,     -- When actually posted
    auto_generated BOOLEAN DEFAULT FALSE,   -- Generated by worker

    -- Status
    status VARCHAR(30) DEFAULT 'draft',     -- draft, scheduled, pending_approval, posting, published, failed, deleted, expired
    tiktok_post_id VARCHAR(100),            -- TikTok's ID after posting
    tiktok_url TEXT,                        -- Direct URL to posted TikTok
    error_message TEXT,                     -- Error message if failed
    retry_count INTEGER DEFAULT 0,          -- Number of posting retries

    -- Analytics (JSONB for flexibility)
    analytics JSONB DEFAULT '{
        "views": 0,
        "likes": 0,
        "shares": 0,
        "comments": 0,
        "saves": 0,
        "engagement_rate": 0,
        "reach": 0,
        "profile_visits": 0
    }'::jsonb,

    -- Metadata
    created_by UUID REFERENCES users(id) ON DELETE SET NULL,
    approved_by UUID REFERENCES users(id) ON DELETE SET NULL,

    -- Timestamps
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Comments for documentation
COMMENT ON TABLE tiktok_posts IS 'TikTok posts for dog awareness campaigns';
COMMENT ON COLUMN tiktok_posts.dog_id IS 'Reference to the dog this post is about';
COMMENT ON COLUMN tiktok_posts.template_type IS 'Template used: urgent_countdown, longest_waiting, overlooked_angel, foster_plea, happy_update, educational';
COMMENT ON COLUMN tiktok_posts.status IS 'Post lifecycle status: draft, scheduled, pending_approval, posting, published, failed, deleted, expired';
COMMENT ON COLUMN tiktok_posts.analytics IS 'JSON object storing engagement metrics from TikTok API';
COMMENT ON COLUMN tiktok_posts.auto_generated IS 'True if post was auto-generated by the background worker';

-- ============================================================================
-- TIKTOK_TEMPLATES TABLE
-- Purpose: Store custom templates (supplements defaults defined in code)
-- ============================================================================
CREATE TABLE IF NOT EXISTS tiktok_templates (
    -- Primary Key
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Identity
    template_id VARCHAR(100) UNIQUE NOT NULL, -- Custom identifier (e.g., shelter_urgent_tx)
    name VARCHAR(255) NOT NULL,             -- Display name
    type VARCHAR(50) NOT NULL,              -- urgent_countdown, longest_waiting, etc.
    description TEXT,                       -- Template description

    -- Caption Template
    caption_template TEXT NOT NULL,         -- Caption with {placeholders}
    required_placeholders TEXT[] DEFAULT '{}', -- Placeholders that must be filled
    optional_placeholders TEXT[] DEFAULT '{}', -- Placeholders that are optional

    -- Hashtags
    default_hashtags TEXT[] DEFAULT '{}',   -- Always-included hashtags
    optional_hashtags TEXT[] DEFAULT '{}',  -- May-be-included hashtags
    max_hashtags INTEGER DEFAULT 10,        -- Maximum hashtags to use

    -- Styling
    overlay_style VARCHAR(50),              -- Overlay style preset
    suggested_music_id VARCHAR(100),        -- Suggested TikTok sound
    suggested_music_name VARCHAR(255),      -- Sound display name
    include_countdown BOOLEAN DEFAULT FALSE,
    include_wait_time BOOLEAN DEFAULT FALSE,
    text_overlay_template TEXT,             -- Overlay text template

    -- Timing
    optimal_post_hours INTEGER[] DEFAULT '{9,12,17,20}', -- Hours (0-23)
    auto_generate_enabled BOOLEAN DEFAULT FALSE,
    priority INTEGER DEFAULT 0,             -- Higher = more important

    -- Ownership
    shelter_id UUID REFERENCES shelters(id) ON DELETE CASCADE, -- NULL = global
    created_by UUID REFERENCES users(id) ON DELETE SET NULL,

    -- State
    is_active BOOLEAN DEFAULT TRUE,

    -- Timestamps
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

