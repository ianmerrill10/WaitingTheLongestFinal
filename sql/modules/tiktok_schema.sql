-- Function: Record analytics snapshot
CREATE OR REPLACE FUNCTION record_tiktok_analytics_snapshot(p_post_id UUID)
RETURNS VOID AS $$
DECLARE
    v_analytics JSONB;
BEGIN
    SELECT analytics INTO v_analytics
    FROM tiktok_posts
    WHERE id = p_post_id;

    IF v_analytics IS NOT NULL THEN
        INSERT INTO tiktok_analytics_history (
            post_id, views, likes, shares, comments, saves,
            engagement_rate, reach, profile_visits
        ) VALUES (
            p_post_id,
            (v_analytics->>'views')::bigint,
            (v_analytics->>'likes')::bigint,
            (v_analytics->>'shares')::bigint,
            (v_analytics->>'comments')::bigint,
            (v_analytics->>'saves')::bigint,
            (v_analytics->>'engagement_rate')::decimal,
            (v_analytics->>'reach')::bigint,
            (v_analytics->>'profile_visits')::bigint
        );
    END IF;
END;
$$ LANGUAGE plpgsql;

COMMENT ON FUNCTION record_tiktok_analytics_snapshot IS 'Records current analytics as a historical snapshot';

-- Function: Get posts needing analytics update
CREATE OR REPLACE FUNCTION get_tiktok_posts_for_analytics_update(p_limit INTEGER DEFAULT 50)
RETURNS SETOF tiktok_posts AS $$
BEGIN
    RETURN QUERY
    SELECT *
    FROM tiktok_posts
    WHERE status = 'published'
      AND tiktok_post_id IS NOT NULL
      AND (
          (analytics->>'last_updated') IS NULL
          OR (analytics->>'last_updated')::timestamp < NOW() - INTERVAL '1 hour'
      )
    ORDER BY
        -- Prioritize posts that haven't been updated recently
        (analytics->>'last_updated')::timestamp NULLS FIRST,
        -- Then by engagement (more popular posts get updated more often)
        (analytics->>'views')::bigint DESC
    LIMIT p_limit;
END;
$$ LANGUAGE plpgsql;

COMMENT ON FUNCTION get_tiktok_posts_for_analytics_update IS 'Returns posts that need analytics refresh from TikTok API';

-- Function: Cleanup old analytics history
CREATE OR REPLACE FUNCTION cleanup_tiktok_analytics_history(p_retention_days INTEGER DEFAULT 90)
RETURNS INTEGER AS $$
DECLARE
    v_deleted INTEGER;
BEGIN
    DELETE FROM tiktok_analytics_history
    WHERE recorded_at < NOW() - (p_retention_days || ' days')::interval;

    GET DIAGNOSTICS v_deleted = ROW_COUNT;
    RETURN v_deleted;
END;
$$ LANGUAGE plpgsql;

COMMENT ON FUNCTION cleanup_tiktok_analytics_history IS 'Removes analytics history older than specified days (default 90)';

-- Function: Mark expired scheduled posts
CREATE OR REPLACE FUNCTION mark_expired_tiktok_posts()
RETURNS INTEGER AS $$
DECLARE
    v_updated INTEGER;
BEGIN
    UPDATE tiktok_posts
    SET status = 'expired', updated_at = NOW()
    WHERE status = 'scheduled'
      AND scheduled_at < NOW() - INTERVAL '1 hour';

    GET DIAGNOSTICS v_updated = ROW_COUNT;
    RETURN v_updated;
END;
$$ LANGUAGE plpgsql;

COMMENT ON FUNCTION mark_expired_tiktok_posts IS 'Marks scheduled posts as expired if they missed their posting window';

