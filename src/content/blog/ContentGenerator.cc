std::string MarkdownGenerator::loadTemplate(const std::string& template_name) {
    std::lock_guard<std::mutex> lock(mutex_);

    // Check cache first
    auto it = template_cache_.find(template_name);
    if (it != template_cache_.end()) {
        return it->second;
    }

    // Load from file
    std::string path = config_.template_directory + "/" + template_name;
    std::ifstream file(path);

    if (!file.is_open()) {
        throw std::runtime_error("Failed to load template: " + path);
    }

    std::ostringstream ss;
    ss << file.rdbuf();
    std::string content = ss.str();

    // Cache for future use
    template_cache_[template_name] = content;

    return content;
}

std::string MarkdownGenerator::applyPlaceholders(
    const std::string& template_content,
    const std::map<std::string, std::string>& placeholders
) {
    std::string result = template_content;

    for (const auto& [key, value] : placeholders) {
        std::string placeholder = "{{" + key + "}}";
        size_t pos = 0;
        while ((pos = result.find(placeholder, pos)) != std::string::npos) {
            result.replace(pos, placeholder.length(), value);
            pos += value.length();
        }
    }

    return result;
}

std::string MarkdownGenerator::generateExcerpt(const std::string& content, size_t max_length) {
    std::string text = stripMarkdown(content);

    // Trim whitespace
    size_t start = text.find_first_not_of(" \t\n\r");
    if (start == std::string::npos) return "";
    size_t end = text.find_last_not_of(" \t\n\r");
    text = text.substr(start, end - start + 1);

    return truncateAtWord(text, max_length);
}

std::string MarkdownGenerator::formatDate(
    const std::chrono::system_clock::time_point& tp,
    const std::string& format
) {
    std::time_t time = std::chrono::system_clock::to_time_t(tp);
    std::tm tm;

#ifdef _WIN32
    localtime_s(&tm, &time);
#else
    localtime_r(&time, &tm);
#endif

    std::ostringstream ss;
    ss << std::put_time(&tm, format.c_str());
    return ss.str();
}

std::string MarkdownGenerator::formatWaitTime(const std::chrono::system_clock::time_point& intake_date) {
    auto now = std::chrono::system_clock::now();
    auto duration = now - intake_date;

    auto days = std::chrono::duration_cast<std::chrono::hours>(duration).count() / 24;

    if (days < 1) {
        return "less than a day";
    } else if (days == 1) {
        return "1 day";
    } else if (days < 7) {
        return std::to_string(days) + " days";
    } else if (days < 30) {
        int weeks = static_cast<int>(days / 7);
        return weeks == 1 ? "1 week" : std::to_string(weeks) + " weeks";
    } else if (days < 365) {
        int months = static_cast<int>(days / 30);
        return months == 1 ? "1 month" : std::to_string(months) + " months";
    } else {
        int years = static_cast<int>(days / 365);
        int remaining_months = static_cast<int>((days % 365) / 30);
        if (remaining_months > 0) {
            return std::to_string(years) + " year" + (years > 1 ? "s" : "") +
                " and " + std::to_string(remaining_months) + " month" + (remaining_months > 1 ? "s" : "");
        }
        return years == 1 ? "1 year" : std::to_string(years) + " years";
    }
}

std::string MarkdownGenerator::formatCountdown(const std::chrono::system_clock::time_point& euthanasia_date) {
    auto now = std::chrono::system_clock::now();
    auto duration = euthanasia_date - now;

    if (duration.count() <= 0) {
        return "TIME'S UP";
    }

    auto hours = std::chrono::duration_cast<std::chrono::hours>(duration).count();
    auto days = hours / 24;
    hours = hours % 24;

    if (days > 0) {
        return std::to_string(days) + " day" + (days > 1 ? "s" : "") +
            ", " + std::to_string(hours) + " hour" + (hours > 1 ? "s" : "");
    } else {
        return std::to_string(hours) + " hour" + (hours > 1 ? "s" : "");
    }
}

