BlogPost MarkdownGenerator::generateEducationalPost(
    const std::string& topic,
    const Json::Value& custom_data
) {
    BlogPost post;

    // Map topic to title and content
    std::map<std::string, std::string> topic_titles = {
        {"puppy-care", "Complete Guide to Puppy Care"},
        {"adult-dog-care", "Caring for Your Adult Dog"},
        {"senior-dog-care", "Caring for Senior Dogs: A Complete Guide"},
        {"health-basics", "Dog Health Basics Every Owner Should Know"},
        {"training-101", "Dog Training 101: Essential Commands"},
        {"nutrition-guide", "Dog Nutrition: What to Feed Your Pup"},
        {"first-time-owner", "First-Time Dog Owner's Guide"},
        {"adoption-prep", "Preparing Your Home for a New Dog"}
    };

    auto title_it = topic_titles.find(topic);
    post.title = (title_it != topic_titles.end()) ?
        title_it->second : "Dog Care Guide: " + topic;

    post.slug = BlogPost::generateSlug(post.title);

    // Generate content
    post.content = generateEducationalContent(topic, custom_data);

    // Generate excerpt
    post.excerpt = generateExcerpt(post.content, config_.max_excerpt_length);

    // Classification
    post.category = BlogCategory::EDUCATIONAL;
    post.tags = {"educational", "dog-care", topic};

    // Status
    post.status = PostStatus::DRAFT;

    // SEO
    post.meta_description = truncateAtWord(stripMarkdown(post.content), config_.max_meta_description_length);

    // Author
    post.author_name = config_.default_author;
    post.is_auto_generated = true;

    // Timestamps
    post.created_at = std::chrono::system_clock::now();
    post.updated_at = post.created_at;

    return post;
}

std::string MarkdownGenerator::generateEducationalContent(
    const std::string& topic,
    const Json::Value& custom_data
) {
    // Try to load topic-specific template
    std::string template_name = "educational/" + topic + ".md.template";
    std::string template_content;

    try {
        template_content = loadTemplate(template_name);
    } catch (...) {
        // Generic educational template
        template_content = R"(# {{TITLE}}

{{INTRODUCTION}}

## Key Points

{{KEY_POINTS}}

## Getting Started

{{GETTING_STARTED}}

## Tips & Best Practices

{{TIPS}}

## Conclusion

{{CONCLUSION}}

---

*Have questions? Our community is here to help you on your dog parenting journey!*
)";
    }

    std::map<std::string, std::string> placeholders;

    // Use custom data if provided
    if (custom_data.isMember("title")) {
        placeholders["TITLE"] = custom_data["title"].asString();
    } else {
        placeholders["TITLE"] = "Dog Care Guide";
    }

    if (custom_data.isMember("introduction")) {
        placeholders["INTRODUCTION"] = custom_data["introduction"].asString();
    } else {
        placeholders["INTRODUCTION"] = "Taking care of a dog is a rewarding experience that "
            "requires knowledge and commitment. This guide will help you provide the best care for your canine companion.";
    }

    if (custom_data.isMember("key_points") && custom_data["key_points"].isArray()) {
        std::ostringstream points;
        for (const auto& point : custom_data["key_points"]) {
            points << "- " << point.asString() << "\n";
        }
        placeholders["KEY_POINTS"] = points.str();
    } else {
        placeholders["KEY_POINTS"] = "- Provide proper nutrition\n"
            "- Ensure regular exercise\n"
            "- Schedule veterinary checkups\n"
            "- Give plenty of love and attention\n";
    }

    if (custom_data.isMember("getting_started")) {
        placeholders["GETTING_STARTED"] = custom_data["getting_started"].asString();
    } else {
        placeholders["GETTING_STARTED"] = "Start by understanding your dog's specific needs based "
            "on their breed, age, and health condition. Every dog is unique!";
    }

    if (custom_data.isMember("tips")) {
        placeholders["TIPS"] = custom_data["tips"].asString();
    } else {
        placeholders["TIPS"] = "1. Establish a routine\n"
            "2. Be patient and consistent\n"
            "3. Use positive reinforcement\n"
            "4. Monitor your dog's health\n"
            "5. Build a strong bond through quality time\n";
    }

    if (custom_data.isMember("conclusion")) {
        placeholders["CONCLUSION"] = custom_data["conclusion"].asString();
    } else {
        placeholders["CONCLUSION"] = "Remember, every dog deserves love, care, and patience. "
            "With the right knowledge and dedication, you can provide a wonderful life for your furry friend.";
    }

    return applyPlaceholders(template_content, placeholders);
}

