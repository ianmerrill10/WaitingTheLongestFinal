BlogPost MarkdownGenerator::generateDogFeature(
    const wtl::core::models::Dog& dog,
    const std::optional<wtl::core::models::Shelter>& shelter
) {
    BlogPost post;

    // Generate unique ID
    post.id = "";  // Will be generated by database

    // Generate title and slug
    post.title = generateDogFeatureTitle(dog);
    post.slug = BlogPost::generateSlug(post.title);

    // Generate content
    post.content = generateDogFeatureContent(dog, shelter);

    // Generate excerpt
    post.excerpt = generateExcerpt(post.content, config_.max_excerpt_length);

    // Classification
    if (dog.urgency_level == "critical" || dog.urgency_level == "high") {
        post.category = BlogCategory::URGENT;
    } else {
        post.category = BlogCategory::LONGEST_WAITING;
    }

    // Tags based on dog attributes
    post.tags = generateDogKeywords(dog);

    // Media
    if (!dog.photo_urls.empty()) {
        post.featured_image = dog.photo_urls[0];
        post.gallery_images = dog.photo_urls;
    }

    // Relationships
    post.dog_id = dog.id;
    if (shelter) {
        post.shelter_id = shelter->id;
    }

    // Status - default to draft
    post.status = PostStatus::DRAFT;

    // SEO
    auto seo = generateDogFeatureSeo(dog, shelter);
    post.meta_description = seo.description;
    post.og_title = seo.og_title;
    post.og_description = seo.og_description;
    post.og_image = seo.og_image;

    // Author
    post.author_name = config_.default_author;
    post.is_auto_generated = true;

    // Timestamps
    post.created_at = std::chrono::system_clock::now();
    post.updated_at = post.created_at;

    return post;
}

std::string MarkdownGenerator::generateDogFeatureContent(
    const wtl::core::models::Dog& dog,
    const std::optional<wtl::core::models::Shelter>& shelter
) {
    // Try to load template, fall back to inline template
    std::string template_content;
    try {
        template_content = loadTemplate("dog_feature.md.template");
    } catch (...) {
        // Inline fallback template
        template_content = R"(# Meet {{DOG_NAME}}: {{DOG_TAGLINE}}

{{FEATURED_IMAGE}}

## About {{DOG_NAME}}

{{DOG_DESCRIPTION}}

**{{DOG_NAME}} has been waiting {{WAIT_TIME}} for a forever home.**

## Quick Facts

{{DOG_ATTRIBUTES}}

## Personality & Temperament

{{PERSONALITY_SECTION}}

{{PHOTO_GALLERY}}

## Why {{DOG_NAME}} Needs You

{{WHY_ADOPT}}

{{CTA_SECTION}}

---

*Last updated: {{LAST_UPDATED}}*
)";
    }

    // Build placeholder map
    std::map<std::string, std::string> placeholders;

    // Basic info
    placeholders["DOG_NAME"] = dog.name;
    placeholders["DOG_ID"] = dog.id;

    // Generate tagline based on breed/age
    std::string tagline;
    if (dog.age_category == "puppy") {
        tagline = "An Adorable " + dog.breed_primary + " Puppy";
    } else if (dog.age_category == "senior") {
        tagline = "A Sweet Senior " + dog.breed_primary;
    } else {
        tagline = "A Wonderful " + dog.breed_primary;
    }
    placeholders["DOG_TAGLINE"] = tagline;

    // Featured image
    if (!dog.photo_urls.empty()) {
        placeholders["FEATURED_IMAGE"] = "![" + dog.name + "](" + dog.photo_urls[0] + ")";
    } else {
        placeholders["FEATURED_IMAGE"] = "";
    }

    // Description
    placeholders["DOG_DESCRIPTION"] = dog.description.empty() ?
        dog.name + " is looking for their perfect match!" : dog.description;

    // Wait time
    placeholders["WAIT_TIME"] = formatWaitTime(dog.intake_date);

    // Attributes
    placeholders["DOG_ATTRIBUTES"] = generateDogAttributes(dog);

    // Personality
    placeholders["PERSONALITY_SECTION"] = generatePersonalitySection(dog);

    // Photo gallery
    if (dog.photo_urls.size() > 1) {
        placeholders["PHOTO_GALLERY"] = "## More Photos\n\n" + generatePhotoGallery(dog.photo_urls);
    } else {
        placeholders["PHOTO_GALLERY"] = "";
    }

    // Why adopt section based on urgency
    if (dog.urgency_level == "critical") {
        placeholders["WHY_ADOPT"] = "**URGENT:** " + dog.name + " is in a critical situation and needs "
            "immediate help. Without rescue or adoption, this sweet pup may not have much time left. "
            "Can you open your heart and home?";
    } else if (dog.urgency_level == "high") {
        placeholders["WHY_ADOPT"] = dog.name + " is at high risk and desperately needs a "
            "loving home. Every day matters for dogs like " + dog.name + ".";
    } else {
        placeholders["WHY_ADOPT"] = dog.name + " has been patiently waiting and would make "
            "a wonderful addition to the right family. Don't let this amazing pup be overlooked!";
    }

    // CTA section
    placeholders["CTA_SECTION"] = generateCtaSection(dog, shelter);

    // Last updated
    placeholders["LAST_UPDATED"] = formatDate(std::chrono::system_clock::now());

    return applyPlaceholders(template_content, placeholders);
}

