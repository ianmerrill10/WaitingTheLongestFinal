BlogPost MarkdownGenerator::generateUrgentRoundup(
    const std::vector<wtl::core::models::Dog>& dogs,
    const std::chrono::system_clock::time_point& date
) {
    BlogPost post;

    // Generate date-based title
    std::string date_str = formatDate(date, "%B %d, %Y");
    post.title = "Urgent Dogs Needing Help Today - " + date_str;
    post.slug = BlogPost::generateSlug("urgent-dogs-" + formatDate(date, "%Y-%m-%d"));

    // Generate content
    post.content = generateUrgentRoundupContent(dogs, date);

    // Generate excerpt
    std::string excerpt = std::to_string(dogs.size()) + " dogs need immediate help today. "
        "These pups are running out of time and desperately need rescue, foster, or adoption.";
    post.excerpt = excerpt;

    // Classification
    post.category = BlogCategory::URGENT;
    post.tags = {"urgent", "rescue", "euthanasia-list", "critical", "help-needed"};

    // Use first dog's photo as featured
    if (!dogs.empty() && !dogs[0].photo_urls.empty()) {
        post.featured_image = dogs[0].photo_urls[0];
    }

    // Status
    post.status = PostStatus::DRAFT;

    // SEO
    auto seo = generateUrgentRoundupSeo(static_cast<int>(dogs.size()), date);
    post.meta_description = seo.description;
    post.og_title = seo.og_title;
    post.og_description = seo.og_description;

    // Author
    post.author_name = config_.default_author;
    post.is_auto_generated = true;

    // Timestamps
    post.created_at = std::chrono::system_clock::now();
    post.updated_at = post.created_at;

    return post;
}

